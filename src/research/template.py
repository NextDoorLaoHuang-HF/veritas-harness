def generate_template(report_type: str, topic: str) -> str:
    """Generate a draft template for the given report type and topic."""
    template = TEMPLATES.get(report_type, TEMPLATES["general"])
    today = date.today().isoformat()
    timestamp = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    return template.format(topic=topic, date=today, timestamp=timestamp)


def cmd_template(args):
    """Handle `veritas template` CLI command."""
    report_type = args.type or "general"
    topic = args.topic or "研究主题"

    if report_type not in TEMPLATES:
        print(f"Error: unknown type '{report_type}'. Choose from: {', '.join(TEMPLATES.keys())}")
        return

    content = generate_template(report_type, topic)

    if args.output:
        Path(args.output).parent.mkdir(parents=True, exist_ok=True)
        Path(args.output).write_text(content)
        print(f"✔ Template written to {args.output}")
    elif args.run_dir:
        run_dir = Path(args.run_dir)
        run_dir.mkdir(parents=True, exist_ok=True)
        out_path = run_dir / "draft-report.md"
        out_path.write_text(content)
        print(f"✔ Template written to {out_path}")
    else:
        print(content)
